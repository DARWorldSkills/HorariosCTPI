package com.aprendiz.ragp.horariosctpi.models;

public class Iconos {
    private String nombre;
    private String manana;
    private String tarde;
    private String noche;
    private String verde;
    private String naranja;
    private String azul;
    private String blanco;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getManana() {
        return manana;
    }

    public void setManana(String manana) {
        this.manana = manana;
    }

    public String getTarde() {
        return tarde;
    }

    public void setTarde(String tarde) {
        this.tarde = tarde;
    }

    public String getNoche() {
        return noche;
    }

    public void setNoche(String noche) {
        this.noche = noche;
    }

    public String getVerde() {
        return verde;
    }

    public void setVerde(String verde) {
        this.verde = verde;
    }

    public String getNaranja() {
        return naranja;
    }

    public void setNaranja(String naranja) {
        this.naranja = naranja;
    }

    public String getAzul() {
        return azul;
    }

    public void setAzul(String azul) {
        this.azul = azul;
    }

    public String getBlanco() {
        return blanco;
    }

    public void setBlanco(String blanco) {
        this.blanco = blanco;
    }
}
